#include <iostream>
using namespace std;

int main() {
    
    int num1, num2, suma;
    
    cout << "Ingresa el primer numero: ";
    cin >> num1;
    
    cout << "Ingresa el segundo numero: ";
    cin >> num2;
    
    suma = num1 + num2;
    
    cout << "La suma de " << num1 << " + " << num2 << " = " << suma << endl;
    
    return 0;
}
